const baseUrl = 'https://ethgasstation.info/api/'
const baseUrlEthereumGasStation = baseUrl + 'ethgasAPI.json'

/* //////////////////////////////////////////////////////////////////////// */
/* */
/* //////////////////////////////////////////////////////////////////////// */

const getEthereumGasPrice = async () => {
    let responseFrom = await fetch(baseUrlEthereumGasStation)
    let response = await responseFrom.json()
    let info = {
        high: parseFloat(response.fast / 10),
        medium: parseFloat(response.average / 10),
        low: parseFloat(response.safeLow / 10),
    }
    return info
}

/* //////////////////////////////////////////////////////////////////////// */
/* */
/* //////////////////////////////////////////////////////////////////////// */

export default {
    getEthereumGasPrice
}

