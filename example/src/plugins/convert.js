import Utils from 'web3-utils'
import PluginBN from './plugin-bignumber'

/* //////////////////////////////////////////////////////////////////////// */
/* */
/* //////////////////////////////////////////////////////////////////////// */

const convertWeiToEth = (val) => {
    val = PluginBN(val).toString(10)
    return Utils.fromWei(val, 'ether');
}

const convertWeiToGwei = (val) => {
    val = PluginBN(val).toString(10)
    return Utils.fromWei(val, 'gwei')
}

const convertGweiToWei = (val) => {
    val = PluginBN(val).toString(10)
    return Utils.toWei(val, 'gwei')
}

/* //////////////////////////////////////////////////////////////////////// */
/* */
/* //////////////////////////////////////////////////////////////////////// */

export default {
    convertWeiToEth,
    convertWeiToGwei,
    convertGweiToWei
}

